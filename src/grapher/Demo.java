    public String difrentiate(String io){
    String s=io.replaceAll(" ","");
int x=s.length();
int t=0;

for(int i=0;i<x;i++){
    if(s.charAt(i)=='i' || s.charAt(i)=='c' || s.charAt(i)=='t' || s.charAt(i)=='l'){
        t++;
    }
}
String kol="";
int d=0;
for(int i=0;i<x;i++){
    if(d<=t){
         if(s.charAt(i)=='s'){if(s.charAt(i+1)=='i'){if(s.charAt(i+2)=='n')kol=kol+"cos"+s.substring(i+3,x);d++;}}
        if(s.charAt(i)=='c'){if(s.charAt(i+1)=='o'){if(s.charAt(i+2)=='s')kol=kol+"(-)sin"+s.substring(i+3,x);d++;}}
        if(s.charAt(i)=='t'){if(s.charAt(i+1)=='a'){if(s.charAt(i+2)=='n')kol=kol+"sec^2"+s.substring(i+3,x);d++;}}
        if(s.charAt(i)=='l' && s.charAt(i+1)=='o' && s.charAt(i+2)=='g'){kol=kol+"1/"+s.substring(i+3,x);d++;}
}
}
    return kol;
}
